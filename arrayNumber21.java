
import java.util.Scanner;

public class arrayNumber21 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int[] num = {5, 12, 7, 20};
       

        for (int i = 0; i <= 4; i++) {
            System.out.println(num[i]);
        }

        sc.close();
    }
}
